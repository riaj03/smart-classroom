    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {

        $studentId = Student::where('user_id', $id)->first();
        $student = StudentCourse::where('student_id', $studentId['id'])->with('course')->get();
        return view('admin.students.profile', compact('student'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $students = Student::findOrFail($id);
        return view('admin.students.edit', compact('students'));
    }

